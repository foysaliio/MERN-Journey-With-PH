const month = 5;

if (month === 1 || month === 2) {
  console.log("Summer");
} else if (month === 3 || month === 4) {
  console.log("Monsoon");
} else if (month === 5 || month === 6) {
  console.log("Autumn");
} else if (month === 7 || month === 8) {
  console.log("Late Autumn");
} else if (month === 9 || month === 10) {
  console.log("Winter");
} else if (month === 11 || month === 12) {
  console.log("Spring");
} else {
  console.log("Invalid Season");
}
